export function localizeBankingModule(module: BankingModule, locale: AppLocale): BankingModule {
  const localized = moduleTranslations[locale]?.[module.slug];

  if (!localized) {
    return module;
  }

  return {
    ...module,
    name: localized.name,
    summary: localized.summary
  };
}
